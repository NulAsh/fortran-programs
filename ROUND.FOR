C ROUNDING MODULE

      INTEGER FUNCTION FLOOR(X)
      FLOOR=INT(X)
      IF (X.LT.0.AND.INT(X).NE.X) FLOOR=FLOOR-1
      RETURN
      END

      INTEGER FUNCTION CEILING(X)
      INTEGER FLOOR
      CEILING=-FLOOR(-X)
      RETURN
      END

C  ROUNDING TOWARDS POSITIVE INFINITY
      INTEGER FUNCTION RPI(X)
      INTEGER FLOOR
      RPI=FLOOR(X+0.5)
      RETURN
      END

C  ROUNDING TOWARDS NEGATIVE INFINITY
      INTEGER FUNCTION RNI(X)
      INTEGER CEILING
      RNI=CEILING(X-0.5)
      RETURN
      END

C  ROUNDING TOWARDS EVEN
      INTEGER FUNCTION RTE(X)
      INTEGER FLOOR,CEILING
      Y=(2*X-1)/4
      RTE=FLOOR(X+0.5)+CEILING(Y)-FLOOR(Y)-1
      RETURN
      END
